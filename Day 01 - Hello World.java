
public class Solution {
	public static void main(String[] args) {

		Scanner scan = new Scanner(System.in);   //creates scanner object to read input from stdin
		String inputString = scan.nextLine();    //read string input and save to variable "inputString"
		scan.close();                            //closes scanner object
      
		System.out.println("Hello, World.");     //prints Hello, World. as output 
    System.out.println(inputString);         //prints inputString variable
	}
}
